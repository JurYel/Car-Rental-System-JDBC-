    private void jtxt_SearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxt_SearchKeyTyped
        String search = jtxt_Search.getText().trim();
        try{
            if(search.isEmpty()){
                ArrayList<Driver> list = driverDAO.getAllDrivers();
                DriverTableModel model = new DriverTableModel(list);
                jtbl_Drivers.setModel(model);
            }
            else{
                ArrayList<Driver> list = driverDAO.searchDriver(search);
                DriverTableModel model = new DriverTableModel(list);
                jtbl_Drivers.setModel(model);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jtxt_SearchKeyTyped

