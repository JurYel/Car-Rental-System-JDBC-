    private void jbtn_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_RegisterActionPerformed
        try{
            String branch = jComboBox_Branch.getSelectedItem().toString();
            String agent = jComboBox_Agent.getSelectedItem().toString();
            String car = jComboBox_Car.getSelectedItem().toString();
            String first = jtxt_First.getText().trim();
            String last = jtxt_Last.getText().trim();
            String contact = jtxt_Contact.getText().trim();
            
            if(first.isEmpty() || contact.isEmpty()){
                JOptionPane.showMessageDialog(DriverUI.this,"Fill up all fields!","Register Error",JOptionPane.ERROR_MESSAGE);
            }
            else{
                int branchID = branchDAO.getBranchID(branch);
                int agentID = agentDAO.getAgentID(agent);
                int carID = carDAO.getCarID(car);
                
                if(driverDAO.checkIfDriver(first + " " + last)){
                    String carDrived = driverDAO.getCarDrived(String.format("%s %s",first,last));
                    JOptionPane.showMessageDialog(DriverUI.this,first + " " + last + " is already a driver of " + carDrived+ "!","Register Error",JOptionPane.ERROR_MESSAGE);
                }
                else{
                    Driver driver = new Driver(branch,agent,car,first,last,contact);
                    driver.setBranchID(branchID);
                    driver.setAgentID(agentID);
                    driver.setCarID(carID);

                    driverDAO.EmployDriver(driver);
                    JOptionPane.showMessageDialog(DriverUI.this,"Driver registered successfully!","Register Success",JOptionPane.INFORMATION_MESSAGE);
                    populateTable();
                    reset();
                }
                
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_RegisterActionPerformed

    private void jbtn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_UpdateActionPerformed
        int row = jtbl_Drivers.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(DriverUI.this,"Select a row","Update Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                String branch = jComboBox_Branch.getSelectedItem().toString();
                String agent = jComboBox_Agent.getSelectedItem().toString();
                String car = jComboBox_Car.getSelectedItem().toString();
                String carRec = jtbl_Drivers.getValueAt(row,2).toString();
                String first = jtxt_First.getText().trim();
                String last = jtxt_Last.getText().trim();
                String contact = jtxt_Contact.getText().trim();

                int branchID = branchDAO.getBranchID(branch);
                    int agentID = agentDAO.getAgentID(agent);
                    int carID = carDAO.getCarID(car);

                    if(driverDAO.checkIfDriver(first + " " + last)){
                        String carDrived = driverDAO.getCarDrived(String.format("%s %s",first,last));
                        JOptionPane.showMessageDialog(DriverUI.this,first + " " + last + " is already a driver of " + carDrived+ "!","Register Error",JOptionPane.ERROR_MESSAGE);
                    }
                    else{
                        if(car.equals(carRec)){
                            Driver driver = new Driver(branch,agent,car,first,last,contact);
                            driver.setBranchID(branchID);
                            driver.setAgentID(agentID);
                            driver.setCarID(carID);

                            driverDAO.UpdateDriver(driver);
                            JOptionPane.showMessageDialog(DriverUI.this,"Driver updated successfully!","Update Success",JOptionPane.INFORMATION_MESSAGE);
                            populateTable();
                            reset();
                        }
                        else{
                            if(driverDAO.checkIfCarHasDriver(carID)){
                                JOptionPane.showMessageDialog(DriverUI.this,"Car already has a driver!","Update Error",JOptionPane.ERROR_MESSAGE);
                            }
                            else{
                                Driver driver = new Driver(branch,agent,car,first,last,contact);
                                driver.setBranchID(branchID);
                                driver.setAgentID(agentID);
                                driver.setCarID(carID);

                                driverDAO.UpdateDriver(driver);
                                JOptionPane.showMessageDialog(DriverUI.this,"Driver updated successfully!","Update Success",JOptionPane.INFORMATION_MESSAGE);
                                populateTable();
                                reset();
                            }
                        }
                    }
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_jbtn_UpdateActionPerformed

