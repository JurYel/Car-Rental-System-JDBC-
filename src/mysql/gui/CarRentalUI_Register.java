    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String first = jtxt_First.getText().trim();
            String last = jtxt_Last.getText().trim();
            String id = jtxt_EmployeeID.getText().trim();
            String pass = jpf_Password.getText().trim();
            
            if(first.equals("First Name") || last.equals("Last Name")
                    || id.equals("Employee ID") || pass.equals("Password")){
                JOptionPane.showMessageDialog(CarRentalUI_Register.this,"Fill up all fields","Register Error",JOptionPane.ERROR_MESSAGE);
            }
            else{
                if(accDAO.checkIfAccountExists(id)){
                    JOptionPane.showMessageDialog(CarRentalUI_Register.this,"Employee of such ID already exists","Login Error",JOptionPane.ERROR_MESSAGE);
                }
                else{
                    Account account = new Account(id,first,last,pass);
                    accDAO.registerAccount(account);
                    reset();
                    
                    CarRentalUI_Login login = new CarRentalUI_Login();
                    setVisible(false);
                    login.setVisible(true);
                    
                }
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

