    private void jbtn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_LoginActionPerformed
        try{
            String employeeID = jtxt_EmployeeID.getText().trim();
            String pass = jpf_Password.getText().trim();
           if(employeeID.equals("EmployeeID...") || pass.equals("Password...")){
                    JOptionPane.showMessageDialog(CarRentalUI_Login.this,"Fill up all fields","Login Error",JOptionPane.ERROR_MESSAGE);
                }
                else{
                    if(accDAO.checkIfAccountExists(employeeID)){
                        if(accDAO.checkPassword(employeeID, pass) ||
                                accDAO.checkEncryptedPassword(employeeID, pass)){
                            HomePageUI homeUI = new HomePageUI(employeeID);
                            setVisible(false);
                            homeUI.setVisible(true);
                        }
                        else{
                            JOptionPane.showMessageDialog(CarRentalUI_Login.this,"Password is incorrect","Login Error",JOptionPane.ERROR_MESSAGE);
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(CarRentalUI_Login.this,"Employee of such ID does not exist","Login Error",JOptionPane.ERROR_MESSAGE);
                    }
                }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_LoginActionPerformed

