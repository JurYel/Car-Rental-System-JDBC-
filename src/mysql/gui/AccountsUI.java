    private void jtbl_AccountsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbl_AccountsMouseClicked
        int row = jtbl_Accounts.getSelectedRow();
        if((evt.getButton() == MouseEvent.BUTTON1) && (evt.getClickCount() == 1)){
            try{
                String id = jtbl_Accounts.getValueAt(row,0).toString();
                String first = jtbl_Accounts.getValueAt(row,1).toString();
                String last = jtbl_Accounts.getValueAt(row,2).toString();
                String pass = "ENCRYPTED";

                jtxt_AccountID.setText(id);
                jtxt_First.setText(first);
                jtxt_Last.setText(last);
                jpf_Password.setText(pass);

                jtxt_AccountID.setEnabled(false);
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
        else if((evt.getButton() == MouseEvent.BUTTON1) && (evt.getClickCount() == 2))
        {
            reset();
        }
        
    }//GEN-LAST:event_jtbl_AccountsMouseClicked

