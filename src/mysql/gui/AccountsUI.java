    private void jbtn_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_RegisterActionPerformed
        try{
            String id = jtxt_AccountID.getText().trim();
            String first = jtxt_First.getText().trim();
            String last = jtxt_Last.getText().trim();
            String pass = jpf_Password.getText().trim();
            
            if(accDAO.checkIfAccountExists(id)){
                JOptionPane.showMessageDialog(AccountsUI.this,"Account already exists!","Register Error",JOptionPane.ERROR_MESSAGE);
            }
            else{
                Account acc = new Account(id,first,last,pass);
                accDAO.registerAccount(acc);
                JOptionPane.showMessageDialog(AccountsUI.this,"Account has been registered successfully!","Register Success",JOptionPane.INFORMATION_MESSAGE);
                reset();
                populateTable();
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jbtn_RegisterActionPerformed

    private void jbtn_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_UpdateActionPerformed
        int row = jtbl_Accounts.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(AccountsUI.this,"Select a row","Update Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
            
                String first = jtxt_First.getText().trim();
                String last = jtxt_Last.getText().trim();
                String pass = jpf_Password.getText().trim();
                String id = accDAO.getAccountID(String.format("%s %s",first,last));
                Account account = new Account(id,first,last,pass);
                if(!(pass.equals("ENCRYPTED"))){
                    AccountsUI_ChangePassword changePass = new AccountsUI_ChangePassword(account,accDAO,AccountsUI.this);
                    changePass.setVisible(true);
                }
                else{
                    accDAO.updateAccount(account);
                    JOptionPane.showMessageDialog(AccountsUI.this,"Account has been updated","Update Success",JOptionPane.INFORMATION_MESSAGE);
                    populateTable();
                    reset();
                }
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_jbtn_UpdateActionPerformed

    private void jbtn_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_DeleteActionPerformed
        int row = jtbl_Accounts.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(AccountsUI.this,"Select a row","Delete Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                String id = jtbl_Accounts.getValueAt(row,0).toString();
                
                if((JOptionPane.showConfirmDialog(AccountsUI.this,
                        "Delete existing account?","Confirm Delete",
                        JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE)) == JOptionPane.YES_OPTION){
                    accDAO.deleteAccount(id);
                    JOptionPane.showMessageDialog(AccountsUI.this,"Account has been deleted","Delete Success",JOptionPane.ERROR_MESSAGE);
                    populateTable();
                    reset();
                }
                
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_jbtn_DeleteActionPerformed

