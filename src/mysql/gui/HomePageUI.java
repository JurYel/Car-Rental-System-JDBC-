    private void populateTable(){
        try{
            ArrayList<Bookings> list = bookDAO.getAllBookings();
            BookingsTableModel model = new BookingsTableModel(list);
            jtbl_Rentals.setModel(model);
            
            if(carsDue > 0){
                String concat = String.format("%s",(carsDue == 1)? " car is ":" cars are ");
                JOptionPane.showMessageDialog(HomePageUI.this,carsDue + concat + " already beyond five(5) days!","Cars Due",JOptionPane.WARNING_MESSAGE);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }
    
