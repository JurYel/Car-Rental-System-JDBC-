    private void jtxt_SearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxt_SearchKeyTyped
        String search = jtxt_Search.getText().trim();
        try{
            if(search.isEmpty()){
                ArrayList<Bookings> list = bookDAO.getAllBookings();
                BookingsTableModel model = new BookingsTableModel(list);
                jtbl_Rentals.setModel(model);
            }
            else{
                ArrayList<Bookings> list = bookDAO.searchBooking(search);
                BookingsTableModel model = new BookingsTableModel(list);
                jtbl_Rentals.setModel(model);
            }
        }
        catch(Exception exc){
            exc.printStackTrace();
        }
    }//GEN-LAST:event_jtxt_SearchKeyTyped

