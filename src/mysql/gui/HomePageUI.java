    private void jbtn_CancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_CancelActionPerformed
        int row = jtbl_Rentals.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(HomePageUI.this,"Select a row","Cancel Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            int bookID = Integer.parseInt(jtbl_Rentals.getValueAt(row,-2).toString());
            String dateRented = jtbl_Rentals.getValueAt(row, 4).toString();
            String dateBooked = jtbl_Rentals.getValueAt(row,3).toString();
            String customer = jtbl_Rentals.getValueAt(row,2).toString();
            String car = jtbl_Rentals.getValueAt(row,1).toString();
            
            try{
                if(dateBooked.equals(" ")){
                    JOptionPane.showMessageDialog(HomePageUI.this,"Unable to cancel booking, it is a rental","Cancel Failed",JOptionPane.ERROR_MESSAGE);
                }
                else if(!(dateBooked.equals(" ")) && !(dateRented.equals(" "))){
                    JOptionPane.showMessageDialog(HomePageUI.this,"Unable to cancel booking, car already rented","Cancel Failed",JOptionPane.ERROR_MESSAGE);
                }
                else if(!(dateBooked.equals(" "))){
                    if((JOptionPane.showConfirmDialog(HomePageUI.this,
                            "Are you to cancel booking?","Confirm Cancellation",
                            JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE)) == JOptionPane.YES_OPTION){
                        bookDAO.cancelBooking(bookID);
                        
                        BookingsHistory history =  new BookingsHistory(null,"Cancelled Booking",customer,car,null);
                        history.setAccountID(accountID);
                        historyDAO.recordAction(history);

                        JOptionPane.showMessageDialog(HomePageUI.this,"Car booking has been cancelled!","Cancel Success",JOptionPane.INFORMATION_MESSAGE);
                        populateTable();
                    }
                }
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
        }
    }//GEN-LAST:event_jbtn_CancelActionPerformed

    private void jbtn_ReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_ReturnActionPerformed
        int row = jtbl_Rentals.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(HomePageUI.this,"Select a row","Return Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                int bookID = Integer.parseInt(jtbl_Rentals.getValueAt(row,-2).toString());
                String carName = jtbl_Rentals.getValueAt(row,1).toString();
                String dateRented = jtbl_Rentals.getValueAt(row,4).toString();
                String customer = jtbl_Rentals.getValueAt(row,2).toString();
                
                if(dateRented.equals(" ")){
                    JOptionPane.showMessageDialog(HomePageUI.this,"Car is not yet rented","Return Error",JOptionPane.ERROR_MESSAGE);
                }
                else if(!(dateRented.equals(" "))){
                    if((JOptionPane.showConfirmDialog(HomePageUI.this,
                            "Return car now?","Confirm Action",
                            JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE)) == JOptionPane.YES_OPTION){
                        String car = carName;
                        bookDAO.returnCar(bookID);
                        
                        BookingsHistory history =  new BookingsHistory(null,"Retured Car",customer,car,null);
                        history.setAccountID(accountID);
                        historyDAO.recordAction(history);

                        JOptionPane.showMessageDialog(HomePageUI.this,car + " has been returned!","Return Success",JOptionPane.INFORMATION_MESSAGE);
                        populateTable();
                    }
                }
            }
            catch(Exception exc){
                exc.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_jbtn_ReturnActionPerformed

